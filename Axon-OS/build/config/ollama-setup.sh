#!/usr/bin/env bash
set -euo pipefail

# Axon OS — Ollama Setup Script
# Installs and configures Ollama with GPU detection for Axon OS

AXON_DIR="${HOME}/.axon"
OLLAMA_CONF="${AXON_DIR}/ollama.conf"
DEFAULT_MODEL="${1:-llama3.2:3b}"

# ---------------------------------------------------------------------------
# detect_gpu
# Checks for NVIDIA or AMD GPU hardware; falls back to CPU.
# Echoes one of: "nvidia" | "amd" | "cpu"
# ---------------------------------------------------------------------------
detect_gpu() {
    if command -v nvidia-smi &>/dev/null && nvidia-smi &>/dev/null 2>&1; then
        echo "nvidia"
        return 0
    fi

    if command -v rocminfo &>/dev/null && rocminfo &>/dev/null 2>&1; then
        echo "amd"
        return 0
    fi

    echo "cpu"
}

# ---------------------------------------------------------------------------
# install_ollama
# Downloads and installs Ollama unless already present.
# ---------------------------------------------------------------------------
install_ollama() {
    if command -v ollama &>/dev/null; then
        echo "[axon] Ollama already installed — skipping download."
        return 0
    fi

    echo "[axon] Installing Ollama..."
    curl -fsSL https://ollama.ai/install.sh | sh
    echo "[axon] Ollama installation complete."
}

# ---------------------------------------------------------------------------
# configure_environment gpu
# Writes OLLAMA_HOST and OLLAMA_NUM_GPU to /etc/environment.
# ---------------------------------------------------------------------------
configure_environment() {
    local gpu="${1}"
    local num_gpu

    case "${gpu}" in
        nvidia|amd) num_gpu=1 ;;
        *)           num_gpu=0 ;;
    esac

    echo "[axon] Configuring /etc/environment for GPU type: ${gpu} (OLLAMA_NUM_GPU=${num_gpu})"

    # Remove existing Ollama entries to avoid duplicates
    sudo sed -i '/^OLLAMA_HOST=/d'    /etc/environment
    sudo sed -i '/^OLLAMA_NUM_GPU=/d' /etc/environment

    {
        echo "OLLAMA_HOST=127.0.0.1:11434"
        echo "OLLAMA_NUM_GPU=${num_gpu}"
    } | sudo tee -a /etc/environment > /dev/null

    echo "[axon] /etc/environment updated."
}

# ---------------------------------------------------------------------------
# setup_service
# Enables and starts ollama.service, then waits for the API to become ready.
# ---------------------------------------------------------------------------
setup_service() {
    echo "[axon] Enabling and starting ollama.service..."
    sudo systemctl enable ollama.service
    sudo systemctl start  ollama.service

    local max_attempts=30
    local attempt=0
    local api_url="http://127.0.0.1:11434/api/tags"

    echo "[axon] Waiting for Ollama API at ${api_url}..."
    while [[ ${attempt} -lt ${max_attempts} ]]; do
        if curl -sf "${api_url}" &>/dev/null; then
            echo "[axon] Ollama API is ready."
            return 0
        fi
        attempt=$(( attempt + 1 ))
        echo "[axon]   ... attempt ${attempt}/${max_attempts}, retrying in 2s"
        sleep 2
    done

    echo "[axon] ERROR: Ollama API did not become ready after ${max_attempts} attempts." >&2
    exit 1
}

# ---------------------------------------------------------------------------
# pull_default_model model
# Pulls the specified model from Ollama registry with up to 3 retries.
# ---------------------------------------------------------------------------
pull_default_model() {
    local model="${1}"
    local max_retries=3
    local attempt=0

    echo "[axon] Pulling model: ${model}"
    while [[ ${attempt} -lt ${max_retries} ]]; do
        if ollama pull "${model}"; then
            echo "[axon] Model '${model}' pulled successfully."
            return 0
        fi
        attempt=$(( attempt + 1 ))
        echo "[axon] Pull attempt ${attempt}/${max_retries} failed. Retrying..." >&2
        sleep 5
    done

    echo "[axon] ERROR: Failed to pull model '${model}' after ${max_retries} attempts." >&2
    exit 1
}

# ---------------------------------------------------------------------------
# write_config gpu model
# Writes Ollama configuration to ~/.axon/ollama.conf
# ---------------------------------------------------------------------------
write_config() {
    local gpu="${1}"
    local model="${2}"

    mkdir -p "${AXON_DIR}"

    cat > "${OLLAMA_CONF}" <<EOF
# Axon OS — Ollama Configuration
# Generated by ollama-setup.sh on $(date -Iseconds)

OLLAMA_HOST=127.0.0.1:11434
OLLAMA_GPU_TYPE=${gpu}
OLLAMA_DEFAULT_MODEL=${model}
EOF

    echo "[axon] Configuration written to ${OLLAMA_CONF}"
}

# ---------------------------------------------------------------------------
# main
# Orchestrates all setup steps in order.
# ---------------------------------------------------------------------------
main() {
    echo "[axon] === Axon OS Ollama Setup ==="

    echo "[axon] Step 1/5: Detecting GPU..."
    local gpu
    gpu="$(detect_gpu)"
    echo "[axon] Detected GPU type: ${gpu}"

    echo "[axon] Step 2/5: Installing Ollama..."
    install_ollama

    echo "[axon] Step 3/5: Configuring environment variables..."
    configure_environment "${gpu}"

    echo "[axon] Step 4/5: Setting up systemd service..."
    setup_service

    echo "[axon] Step 5/5: Pulling default model (${DEFAULT_MODEL})..."
    pull_default_model "${DEFAULT_MODEL}"

    write_config "${gpu}" "${DEFAULT_MODEL}"

    echo "[axon] === Ollama setup complete. GPU: ${gpu}, Model: ${DEFAULT_MODEL} ==="
}

main "$@"
